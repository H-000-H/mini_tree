#!/usr/bin/env python3
"""Kconfig → config.h 生成器

用法:
    python genconfig.py <kconfig_path> <output_dir> [config_file]

若未指定 config_file, 从 output_dir/.config 读取;
若 .config 不存在, 使用 Kconfig 默认值.
"""

import sys
import os
from kconfiglib import Kconfig, standard_sc_expr_str


def main():
    if len(sys.argv) < 3:
        print(__doc__, file=sys.stderr)
        sys.exit(1)

    kconfig_path = sys.argv[1]
    output_dir = sys.argv[2]
    config_file = sys.argv[3] if len(sys.argv) > 3 else os.path.join(output_dir, ".config")
    output_file = os.path.join(output_dir, "config.h")

    os.makedirs(output_dir, exist_ok=True)

    kconf = Kconfig(kconfig_path)

    if os.path.exists(config_file):
        kconf.load_config(config_file)
    else:
        kconf.write_config(config_file)

    with open(output_file, "w") as f:
        f.write("/* SPDX-License-Identifier: MIT */\n")
        f.write("/* Generated by Kconfig — do not edit manually */\n")
        f.write("#ifndef KCONFIG_CONFIG_H\n")
        f.write("#define KCONFIG_CONFIG_H\n\n")

        for sym in kconf.unique_defined_syms:
            if sym.config_string is not None and sym.nodes:
                # 输出 CONFIG_<NAME> 以匹配 .config 格式和 Kbuild 惯例
                name = f"CONFIG_{sym.name}"
                # 只输出可见的 bool/tristate/string/int/hex 选项
                if sym.type in (0x02, 0x03):  # BOOL, TRISTATE
                    if sym.str_value == "y":
                        f.write(f"#define {name} 1\n")
                    elif sym.str_value == "m":
                        f.write(f"#define {name} 1\n")
                elif sym.str_value not in ("n", ""):
                    f.write(f"#define {name} {sym.str_value}\n")

        f.write("\n#endif /* KCONFIG_CONFIG_H */\n")

    print(f"[genconfig] {output_file}")

    # 将 .config 同步回 source tree 同目录 (方便 menuconfig 回写)
    src_config = os.path.join(os.path.dirname(kconfig_path), ".config")
    if os.path.abspath(config_file) != os.path.abspath(src_config):
        import shutil
        shutil.copy2(config_file, src_config)


if __name__ == "__main__":
    main()
